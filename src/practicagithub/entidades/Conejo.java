package practicagithub.entidades;

public class Conejo {
    private String color;
    private float peso;

    public Conejo() {
    }

    public Conejo(String color, float peso) {
        this.color = color;
        this.peso = peso;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }
    
    
}
